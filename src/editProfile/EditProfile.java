    private boolean usernameTaken(String name){
        Reader r = new Reader("players");
        r.read();
        HashSet<String[]> data = r.getData();
        for (String[] strings : data) {
            if (strings[0].equals(name)) {
                return false;
            }
        }
        return true;
    }
    
    private void changeUsernameBtnActionPerformed(java.awt.event.ActionEvent evt) {                                                  
        // TODO add your handling code here:
        if (usernameTaken(username)){
            usernameTakenLabel.setText("Username already taken!");
            wrongpassLabel.setText("");
            succesLabel.setText("");
            return;
        }
        if (passwordLabel.getText().equals(password)) {
            
            Reader r = new Reader(username);
            r.read();
            HashSet<String[]> stream = r.getData();
            File file = new File(System.getProperty("user.dir") + "\\src\\data\\" + username + ".txt"); 
            file.delete();
            String usernameTmp = username;
            username = newUsernameLabel.getText();
            Writter w = new Writter(username);
            w.addStream(stream);
            succesLabel.setText("Successfully updated!");
            wrongpassLabel.setText("");
            usernameTakenLabel.setText("");
            w.updatePlayers(usernameTmp, username);
        }
        else{
            succesLabel.setText("");
            usernameTakenLabel.setText("");
            wrongpassLabel.setText("Wrong password!");
        }
    }    
    
