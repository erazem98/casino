    private void getPassword(){
        Reader r = new Reader("players");
        r.read();
        HashSet<String[]> data = r.getData();
        for (String[] strings : data) {
            if (strings[0].equals(username)) {
                this.password = strings[1];
                break;
            }
        }
    }

