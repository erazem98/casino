    private void changepasswordBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changepasswordBtnActionPerformed
        // TODO add your handling code here:
        if (!password.equals(currentPasswordLabel.getText())) {
            passwordIncorrectLabel.setForeground(new java.awt.Color(255, 0, 0));
            passDontMatchLabel.setText("");
            succesLabel1.setText("");
            return;
        }
        if (!newPasswordLabel.getText().equals(confirmNewPasswordLabel.getText())) {
            passDontMatchLabel.setText("Passwords don't match!");
            passwordIncorrectLabel.setForeground(new java.awt.Color(30, 30, 30));
            succesLabel1.setText("");
            return;
        }
        Writter w = new Writter("players");
        w.updatePlayersPassword(username, newPasswordLabel.getText());
        this.password = newPasswordLabel.getText();
        passDontMatchLabel.setText("");
        passwordIncorrectLabel.setForeground(new java.awt.Color(30, 30, 30));
        succesLabel1.setText("Password successfully changed!");
    }//GEN-LAST:event_changepasswordBtnActionPerformed

