    private void withdrawBtnActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        if(!iscorrect()) {
            messageLabel.setText("Wrong credit card number or CVV2");
            messageLabel.setForeground(new java.awt.Color(255, 0, 0));
            return;
        }
        double amount = Double.parseDouble(amountTextField.getText());
        Writter w = new Writter(username);
        double current = 0;
        Reader r = new Reader(username);
        r.read();
        HashSet<String[]> data = r.getData();
        for (String[] transaction : data) {
            double bet = Double.parseDouble(transaction[0]);
            double win = Double.parseDouble(transaction[1]);
            current += (win - bet);
        }
        if (current - amount >= 0) {
            w.addTransaction(amount, 0);
            messageLabel.setText("Successfully withdrawn! Refresh main screen to see new balance.");
            messageLabel.setForeground(new java.awt.Color(0, 255, 0));
        }
    } 
    
    private boolean iscorrect(){
        String cn = cardTextField.getText();
        String cv = cvvTextfield.getText();
        if (cn.equals("2345348945778900")) {
            if (cv.equals("123")) {
                return true;
            }
        }
        return false;
    }
    
